/* Print Poem */

#include <stdio.h>

int main(void)
{
    printf("\nNothing Gold Can Stay\n");
    printf("Robert Frost - 1872-1963\n\n");
    printf("Nature's first green is gold,\n");
    printf("Her hardest hue to hold.\n");
    printf("Her early leaf's a flower;\n");
    printf("But only so an hour.\n");
    printf("Then leaf subsides to leaf.\n");
    printf("So Eden sank to grief,\n");
    printf("So dawn goes down today.\n");
    printf("Nothing Gold Can Stay.\n\n");

    return 0;
}